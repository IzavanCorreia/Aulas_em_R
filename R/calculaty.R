#' Function somar
#'
#' This function is used to add two values
#'
#' @param a first number.
#' @param b second number.
#'
#' @example
#' somar (200, 100)
#'
#' Console response: 300
#'
#' @export
somar <- function (a,b) {
  a + b;
}

#' Function subtrair
#'
#' This function is used to subtract two values
#'
#' @param a first number.
#' @param b second number.
#'
#' @example
#' subtrair (200, 100)
#'
#' Console response: 100
#'
#' @export
subtrair <- function (a,b) {
  a - b;
}

#' Function multiplicar
#'
#' This function is used to multiply two values
#'
#' @param a first number.
#' @param b second number.
#'
#' @example
#' multiplicar (200, 100)
#'
#' Console response: 20000
#'
#' @export
multiplicar <- function (a,b) {
  a * b;
}

#' Function dividir
#'
#' This function is used to share two values
#'
#' @param a first number.
#' @param b second number.
#'
#' @example
#' dividir (200, 100)
#'
#' Console response: 2
#'
#' @export
dividir <- function (a,b) {
  a / b;
}
